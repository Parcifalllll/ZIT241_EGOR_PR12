# Задаем массив D
D = [1, 2, 3, 4, 5, 6, 7, 8, 9]  # пример массива
n = len(D)

# Вычисляем сумму элементов с нечетными индексами
sum_odd_indices = sum(D[i] for i in range(1, n, 2))

# Выводим массив и сумму
print("Массив D:", D)
print("Сумма элементов с нечетными индексами:", sum_odd_indices)
