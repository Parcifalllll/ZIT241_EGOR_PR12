# Считываем натуральное число N
N = int(input("Введите натуральное число N: "))

# Инициализируем переменные
power = 0
value = 1

# Находим наибольшую степень двойки
while value * 2 <= N:
    value *= 2
    power += 1

# Выводим показатель степени и саму степень
print("Показатель степени:", power)
print("Степень двойки:", value)
